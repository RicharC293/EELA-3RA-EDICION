void main () {

final a = 5;
final b = 10;

// igualdad

final igualdad = a == b;
print(igualdad);

// desigualdad o no igual

// a != 5;
// b != 10

final desigualdad = a != b;
print(desigualdad);

// mayor que
// a>b
// 5 > 10

final menor = a>b;
print(menor);

// menor que
// a<b
// 5 > 10
 final mayor = a<b;
 print(mayor);

 // mayor o igual que
// a >= b
// 5 >= 10

final mayorIgual = a >= b;
print(mayorIgual);

// menor o igual que
// a <= b
// 5 <= 10

final menorIgual = a <= b;
print(menorIgual);

}