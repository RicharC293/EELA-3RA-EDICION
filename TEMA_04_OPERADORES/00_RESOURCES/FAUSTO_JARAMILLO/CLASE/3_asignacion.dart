void main () {

// asignar un valor --> con el signo = 

// asignacion += --> incrementos
// asignacion -= --> decrementos

int tiempoDeJuego = 30;
tiempoDeJuego += 15; // incrementa el valor solicitado (15)
print(tiempoDeJuego);

tiempoDeJuego -= 10; // disminuye el valor solicitado (10) 
print(tiempoDeJuego);

}