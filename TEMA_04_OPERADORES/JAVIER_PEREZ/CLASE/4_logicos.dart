void main(){
  // && and
  final a = true;
  final b = true;
  
  // AND (&&)
  // true && true = true
  // true && false = false
  // false && true = false
  // false && false = false
  print(a && b);


  // || or -> Alt + 124
  // OR (||)
  // true || true = true
  // true || false = true
  // false || true = true
  // false || false = false
  print(a||b); 

  // ! Not
  // NOT (!)
  // !true = false
  // !false = true
  print(!a);
}