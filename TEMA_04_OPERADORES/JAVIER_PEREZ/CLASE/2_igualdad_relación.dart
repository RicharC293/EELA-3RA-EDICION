void main(){
  // Igualdad
  final a = 5;
  final b = 10;
  
  final igualdad = a == b;
  print(igualdad);

  // No es igual
  // a != b
  // 5 != 10
  final desig = a != b;
  print(desig);

  // Mayor que
  // a > b
  // 5 > 10
  final may = a > b;
  print(may);

  // Menor que
  // a < b
  // 5 < 10
  final men = a < b;
  print(men); 

  // Mayor o igual que
  // a >= b
  // 5 >= 10
  final mayi = a >= b;
  print(mayi);

  // Menor o igual que
  // a <= b
  // 5 <= 10
  final meni = a <= b;
  print(meni);
  
   
}