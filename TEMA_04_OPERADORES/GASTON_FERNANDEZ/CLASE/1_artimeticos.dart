void main() {
  // SUMA +
  final suma = 5 + 5;
  print(suma);

  // RESTA -
  final resta = 10 - 3;
  print(resta);

  // MULTIPLICACION 
  final multiplicacion = 2 * 2;
  print(multiplicacion);

  // DIVISION
  // final division = 0/0; NaN
  // final division = 10/0; Infinity
  final(division) = 3 / 2;
  print(division);

  // Division entera
  // 5 | 2
  // -4  2
  // 1
  final divisionEntera = 5 ~/ 2;
  print(divisionEntera);

  // MÃ³dulo
  final modulo = 5 % 2; /// [1,2,3,4,5] -> rojo - amarillo
  print(modulo);

  print("------------");

  // Incremento
  int incrementoA = 1;
  int incrementoB = 1;
  print(incrementoA++); // Postfijo -> postincremento
  print(incrementoA);

  print(++incrementoB); // Prefijo -> preincremento
  print(incrementoB);

  // Decremento







}